public class PasswordPinModel {
    private String password;
    private String pin;

    public void setPassword(String password) {
        this.password = password;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getPassword() {
        return password;
    }

    public String getPin() {
        return pin;
    }
}
